package topologyExtraction;

public class Dependencies {
	String startResource; 
	String endResource;
	String medium; 
	String type; 
	/**
	 * @return the startResource
	 */
	public String getStartResource() {
		return startResource;
	}
	/**
	 * @param startResource the startResource to set
	 */
	public void setStartResource(String startResource) {
		this.startResource = startResource;
	}
	/**
	 * @return the endResource
	 */
	public String getEndResource() {
		return endResource;
	}
	/**
	 * @param endResource the endResource to set
	 */
	public void setEndResource(String endResource) {
		this.endResource = endResource;
	}
	/**
	 * @return the medium
	 */
	public String getMedium() {
		return medium;
	}
	/**
	 * @param medium the medium to set
	 */
	public void setMedium(String medium) {
		this.medium = medium;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	
}
